package org.jsp.methodDemo;

public class Person {
	private int age;
	private String name;
	private String gender;
	private long phone;
	
	public Person(int age, String name, String gender, long phone) {
		this.age = age;
		this.name = name;
		this.gender = gender;
		this.phone = phone;
	}

	public int getAge() {
		return age;
	}

	public void setAge(int age) {
		this.age = age;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getGender() {
		return gender;
	}

	public void setGender(String gender) {
		this.gender = gender;
	}

	public long getPhone() {
		return phone;
	}

	public void setPhone(long phone) {
		this.phone = phone;
	}
	
	
	
	
	
	
		

}
