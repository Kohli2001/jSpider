class program1{
    public static void main(String[] args) {
        System.out.println(10+5); //additions 
        System.out.println(10-5); //substractions 
        System.out.println(10*5); //multiplications 
        System.out.println(10/5); //divisons 
        System.out.println(10%5); //modulus
    }
    
}